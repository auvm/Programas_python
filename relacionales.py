numero_uno = 10
numero_dos = 20

"""
>   mayor qué
<   menor qué
>=  mayor o igual qué
<=  menor o igual qué
==  idéntico o "igual"
!=  diferente
"""
resultado = numero_uno < numero_dos
print(numero_uno, "<", numero_dos, resultado)

resultado = numero_uno > numero_dos
print(numero_uno, ">", numero_dos, resultado)

resultado = numero_uno <= numero_dos
print(numero_uno, "<=", numero_dos, resultado)

resultado = numero_uno >= numero_dos
print(numero_uno, ">=", numero_dos, resultado)

resultado = numero_uno == numero_dos
print(numero_uno, "==", numero_dos, resultado)

resultado = numero_uno != numero_dos
print(numero_uno, "!=", numero_dos, resultado)